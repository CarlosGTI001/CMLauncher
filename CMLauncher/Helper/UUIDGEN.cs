namespace CMLauncher.Helper
{
    static class generador
    {
        public static string UUID()
        {
            Guid myuuid = Guid.NewGuid();
            string myuuidAsString = myuuid.ToString();

            return myuuidAsString.Replace("-", "").ToLower();
        }
    }
}
